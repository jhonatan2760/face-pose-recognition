package com.jhonatansouza.faces.model;

public class FaceRectangle {

    private short top;
    private short left;
    private short widht;
    private short height;

    public short getTop() {
        return top;
    }

    public void setTop(short top) {
        this.top = top;
    }

    public short getLeft() {
        return left;
    }

    public void setLeft(short left) {
        this.left = left;
    }

    public short getWidht() {
        return widht;
    }

    public void setWidht(short widht) {
        this.widht = widht;
    }

    public short getHeight() {
        return height;
    }

    public void setHeight(short height) {
        this.height = height;
    }
}
